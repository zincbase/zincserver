package main

// Slow implementations for big endian architectures.
//
// The actual implementations are in a separate file, to allow them to be tested
// on LE architectures as well.

////////////////////////////////////////////////////////////////////////////////
// Serialization
////////////////////////////////////////////////////////////////////////////////
func SerializeHeader(header *EntryHeader, targetSlice []byte) {
	SerializeHeader_Slow(header, targetSlice)
}

////////////////////////////////////////////////////////////////////////////////
// Deserialization
////////////////////////////////////////////////////////////////////////////////
func DeserializeHeader(headerBytes []byte) *EntryHeader {
	return DeserializeHeader_Slow(headerBytes)
}
